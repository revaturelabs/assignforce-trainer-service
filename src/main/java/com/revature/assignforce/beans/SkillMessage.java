package com.revature.assignforce.beans;

public class SkillMessage {
    private String context;
    private int id;

    public SkillMessage() {
    }

    public String getContext() {
        return context;
    }

    public void setContext(String context) {
        this.context = context;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
